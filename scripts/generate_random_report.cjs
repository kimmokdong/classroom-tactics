const fs = require('fs');
const r = require('./scratch/random_opponent_eval.json');

r.sort((a, b) => parseFloat(b.winRate) - parseFloat(a.winRate));

let table = '| 순위 | 시너지 덱 (AI Draft) | 승률 (vs 무작위 덱) |\n| :---: | :--- | :---: |\n';
r.forEach((x, index) => {
    table += `| ${index + 1} | **${x.name}** | \`${x.winRate}%\` |\n`;
});

const report = `# 🎲 랜덤 덱 상성 시뮬레이터 결과 (AI Draft vs Pure Random)

유저님의 보드는 **역할군 기반 고코스트 조합(AI Draft)**으로 구성하고, 상대방은 시너지 고려 없이 **완전 무작위 8인 조합(Pure Random)**으로 구성했을 때의 시뮬레이션 결과입니다. 

이를 통해 "메타 덱이 아무 생각 없이 짜인 덱을 상대로 얼마나 양학(?)을 잘 하는가"를 볼 수 있습니다.

---

## 🏆 완전 무작위 덱 상대 승률 (양학 티어표)

${table}

---

### 💡 랜덤 덱 상대 분석 및 인사이트
1. **평균 승률의 엄청난 상승!**
   - 기존 '메타 덱 vs 메타 덱' 시뮬레이션에 비해 대부분의 덱 승률이 크게 뛰었습니다! 무작위로 짠 덱은 시너지도, 포지션도 엉망이라서 유저님의 체계적인 AI Draft 덱을 이길 수 없습니다.
2. **양학의 신: 체육(4), 선도부(6), 장난꾸러기(6)**
   - 체육(4)은 승률 82.5%로 사실상 지는 게 불가능한 수준입니다. 장난꾸러기(6) 역시 깡공격력 증가로 무작위 덱을 순식간에 녹여버리며 77.5%의 강력한 양학력을 보여줍니다.
3. **태생적 한계 (도덕 6, 보건부 6)**
   - 재미있게도, 도덕(6)과 보건부(6)는 상대가 무작위 덱이어도 승률이 15~19%로 처참합니다. 퓨어 서포터와 탱커로만 보드를 꽉 채우면, 상대가 아무리 약한 무작위 조합이라 해도 **때려잡을 딜이 부족해서 결국 진다**는 뜻입니다. 이 결과는 유저님의 '서포터 6시너지 약세는 당연하다'는 인사이트를 다시 한번 완벽하게 증명합니다!
`;

fs.writeFileSync('scratch/random_report_temp.md', report, 'utf8');
